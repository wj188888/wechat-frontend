      <p><em>第二条</em> 除另有约定外，本附加险合同保险金的受益人为被保险人本人。</p>
      <h2>保险责任</h2>
      <p><em>第三条</em> 在本附加险合同的保险期限内，被保险人遭受主险合同约定的意外伤害事故，并自事故发生之日起180天内因该事故导致伤害而经认可的医疗机构进行必要的治疗，保险人按下列约定给付意外伤害医疗费用保险金：<br/>
        （1）保险人在扣除本附加险合同中约定的免赔额后，根据被保险人实际支付的合理医疗费用按本附加险合同中约定的给付比例给付意外伤害医疗费用保险金。具体的免赔额、给付比例在保险合同中载明。<br/>
