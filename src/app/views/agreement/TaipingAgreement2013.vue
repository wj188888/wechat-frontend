        （3）保险期间届满被保险人治疗仍未结束的，保险人所负保险责任期限最长可至意外伤害发生之日起第180日止。但累计给付金额达到本附加险合同对应的保险金额时，保险人对该被保险人在本附加险合同项下的保险责任终止。<br/>
        （4）保险人对每一被保险人所负给付意外伤害医疗费用保险金的责任以本附加险合同所载每一被保险人的意外伤害医疗费用保险金额为限，一次或累计给付的保险金达到其本附加险合同项下该被保险人的意外伤害医疗费用保险金额时，保险人对该被保险人在本附加险合同下的保险责任终止。</p>
      <h2>责任免除</h2>
      <p><em>第四条</em> 因下列情形之一，直接或间接导致被保险人发生医疗费用支出的，保险人不承担意外伤害医疗费用保险金给付责任：<br/>
        (一)被保险人身患疾病所支付的费用；<br/>
        (二)被保险人健康护理(含体检、健康体检、疗养、特别护理或静养) 等非治疗性的行为及无客观病征证明其不健康及以捐献身体器官为目的的医疗行为；<br/>
        (三)被保险人流产（但因遭受意外伤害所致不在此限）、堕胎、分娩、不孕症、避孕或绝育手术、变性手术、人体试验和人工生殖，及由此而引起的并发症；<br/>
        (四)被保险人发生的护理（陪住）费、取暖费、交通费、误工费、空调费、膳食费、特需服务费、营养性药品等需要自理的费用；<br/>
        (五)用于矫形、整容、美容、心理咨询、器官移植、角膜屈光成形手术或修复、安装及购买残疾用具（如轮椅、假肢、助听器、假眼、假牙、配镜等）的费用；<br/>
        (六)被保险人在家自设病床治疗；<br/>
        (七)被保险人在非认可的医疗机构治疗发生的医疗费用或保险单签发地社会医疗保险主管部门规定的自费项目；<br/>
        (八)主险合同中列明的“责任免除” 情形导致的医疗费用；<br/>
        (九)其它在保险单或保险凭证中载明的责任免除事项及免赔额、免赔率。</p>
      <h2>保险期间</h2>
      <p><em>第五条</em> 本保险合同保险期间以保险人和投保人协商确定，以保险单载明的起讫时间为准。除另有约定外，本合同的保险期间为一年。<br/>
        若保险人同意，投保人可于每个保险期间届满时或之前，按照续保当时保险人执行的条款和费率向保险人交纳续保保险费，则本保险合同的保险期间将延续一年。<br/>
        保险人有权根据续保当时被保险人的情况决定是否调整续保费率或有条件续保。本保险续保前投保人、被保险人根据保险人的询问应将其已知或已患的疾病或其它情况以书面形式告知保险人。</p>

      <h2>保险事故通知</h2>
      <p><em>第六条</em> 投保人、被保险人或者保险金受益人知道保险事故发生后，应于被保险人入院之日起48小时内或经保险人书面同意延长的期限内通知保险人。<br/>
        被保险人应在认可的医疗机构就诊，若因急诊未在认可的医疗机构就诊的，应在48小时内或经保险人书面同意延长的期限内通知保险人，并根据病情及时转入认可的医疗机构。若确需转入非认可的医疗机构就诊的，应向保险人提出书面申请，保险人在接到申请后三日内给予答复，对于保险人同意在非认可的医疗机构就诊的，对被保险人在非认可的医疗机构发生的住院医疗费用按本附加险合同的规定给付保险金。</p>
      <h2>保险金申请与给付</h2>
      <p><em>第七条</em> 发生保险责任范围内的事故，保险金申请人请求赔偿时，应向保险人提供以下证明和资料。保险金申请人因特殊原因不能提供以下材料的，应提供其他合法有效的材料。保险金申请人未能提供有关材料，导致保险人无法核实的，保险人对无法核实部分不承担给付保险金的责任。<br/>
