      <h1>太平财产保险有限公司<br/>附加意外伤害医疗费用保险条款<br/>（2013版）</h1>
      <h2>总则</h2>
      <p><em>第一条</em> 本附加保险合同（以下简称附加险合同）依主险合同投保人的申请，经保险人同意而订立。本附加险合同作为主险合同的组成部分，主险合同效力终止，本附加险合同效力亦同时终止；主险合同无效，本附加险合同亦无效。本附加险合同与主险合同相抵触之处，以本附加险合同为准，本附加险合同未约定事项，以主险合同为准。<br/>
