      <h2>释义</h2>
      <p><em>1. 认可的医疗机构</em></p>
      <p>在中国境内（不包括香港、澳门、台湾）是指经中华人民共和国卫生部门评审确定的二级或以上的公立医院或投保人与保险人协商共同指定的医院或医疗机构。意外伤害急救不受此限，但经急救情况稳定后，须根据病情及时转入前述指定或认可的医疗机构治疗。</p>
      <p>在中国境外（包括港、澳、台）是指保险人认可的根据所在国家法律规定合法成立、运营并符合以下标准的医疗机构：<br/>
        1）主要运营目的是以住院病人形式提供接待患病、受伤的人并为其提供医疗护理和治疗；<br/>
        2）在一名或若干医生的指导下为病人治疗，其中最少有一名合法执业资格的驻院医生驻诊；<br/>
        3）维持足够妥善的设备为病人提供医学诊断和治疗，并于机构内或由其管理的地方提供进行各种手术的设备；<br/>
        4）有合法执业的护士提供和指导二十四小时的全职护理服务。</p>
      <p>本附加险合同中所指医院不包括以下或类似的医疗机构：<br/>
        1）精神病院；<br/>
        2）老人院、疗养院、戒毒中心和戒酒中心；<br/>
        3）健康中心或天然治疗所、疗养或康复院。</p>
      <p><em>2. 住院</em></p>
      <p>指被保险人因意外伤害，经医生根据临床诊断，必须入住医院之正式病房进行治疗，正式办理入院手续且连续住院二十四小时以上，但不包括入住门诊观察室、家庭病床、其它挂床住院及不合理的住院。如被保险人因非医疗目的自行离开病房12小时（含）以上，视为自动出院。</p>
      <p>挂床住院指被保险人住院过程中一日内未接受与入院诊断相关的检查和治疗，或一日内住院不满二十四小时，遵医嘱到外院接受临时诊疗的除外。</p>
      <p><em>3. 合理医疗费用</em></p>
      <p>在中华人民共和国境外治疗的，保险人承担的医疗费用按保险单签发地相同治疗的平均水平折算。</p>
      <p>在中华人民共和国境内治疗的，指符合保险单签发地政府基本医疗保险报销范围的、合理且必要的医疗费用。</p>

